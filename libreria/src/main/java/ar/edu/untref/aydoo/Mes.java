package ar.edu.untref.aydoo;

public class Mes {

    private int numeroDelMes;
    private int anioDelMes;

    public Mes(int mes, int anio) {
        numeroDelMes = mes;
        anioDelMes = anio;
    }

    public int getNumeroDelMes() {
        return numeroDelMes;
    }

    public int getAnioDelMes() {
        return anioDelMes;
    }
}
